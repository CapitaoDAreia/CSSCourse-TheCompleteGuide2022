function moveUP(){
    let DivItem = document.getElementById('item')
    DivItem.style.transform = "translateY(-100px)"
}
function moveRIGHT(){
    let DivItem = document.getElementById('item')
    DivItem.style.transform = "translateX(100px)"
}
function moveDOWN(){
    let DivItem = document.getElementById('item')
    DivItem.style.transform = "translateY(100px)"
}
function moveLEFT(){
    let DivItem = document.getElementById('item')
    DivItem.style.transform = "translateX(-100px)"
}
function moveCENTER(){
    let DivItem = document.getElementById('item')
    DivItem.style.transform = "translateX(0px)"
}